class Practise_1
{
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a); // 10
        System.out.println(a++); //10
        System.out.println(++a); //12
        System.out.println(a--); //12
        System.out.println(--a); //10
        System.out.println(a); //10
    }
}
