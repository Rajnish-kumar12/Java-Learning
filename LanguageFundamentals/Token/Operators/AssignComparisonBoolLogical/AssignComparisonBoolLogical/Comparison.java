/*
 * Comparison operators
 * ----------------------
 * --> Use to perform comparison between two operands.
 * Ex: ==, !=, >, <, >= <=
 */
class Comparison{
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println(a == b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a != b);
    }
}